// Generates a deterministic lorem-ipsum article dataset shared by the bench
// app(s). Run `node gen.mjs [count]` (default 50). Writes the SAME dataset into
// every target app's src/data.js (vyre, solid, …) so each framework
// renders byte-identical content for a fair comparison.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const COUNT = parseInt(process.argv[2] || '50', 10);

const WORDS =
	'lorem ipsum dolor sit amet consectetur adipiscing elit sed do eiusmod tempor incididunt ut labore et dolore magna aliqua enim ad minim veniam quis nostrud exercitation ullamco laboris nisi aliquip ex ea commodo consequat duis aute irure in reprehenderit voluptate velit esse cillum'.split(
		' ',
	);
const SECTIONS = ['World', 'Business', 'Tech', 'Science', 'Sports', 'Culture', 'Opinion'];

// Tiny deterministic PRNG (mulberry32) so the dataset is stable across runs.
function rng(seed) {
	return () => {
		seed |= 0;
		seed = (seed + 0x6d2b79f5) | 0;
		let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
		t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
		return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
	};
}
const rand = rng(1234);
const pick = (arr) => arr[Math.floor(rand() * arr.length)];
const sentence = (n) => {
	const w = Array.from({ length: n }, () => pick(WORDS));
	w[0] = w[0][0].toUpperCase() + w[0].slice(1);
	return w.join(' ') + '.';
};
const paragraph = (n) =>
	Array.from({ length: n }, () => sentence(8 + Math.floor(rand() * 10))).join(' ');

const articles = Array.from({ length: COUNT }, (_, i) => ({
	id: i + 1,
	section: pick(SECTIONS),
	title: sentence(4 + Math.floor(rand() * 5)).replace(/\.$/, ''),
	byline: 'By ' + pick(WORDS) + ' ' + pick(WORDS),
	lead: sentence(14 + Math.floor(rand() * 8)),
	body1: paragraph(3),
	body2: paragraph(3),
	points: Math.floor(rand() * 500),
}));

const out =
	'// AUTO-GENERATED by benchmarks/news/gen.mjs — do not edit by hand.\n' +
	'export const ARTICLES = ' +
	JSON.stringify(articles) +
	';\n';

// Every target app gets the identical dataset (skip a target whose src/ dir
// doesn't exist yet, so adding a new target is just creating its folder).
const TARGETS = ['vyre', 'solid', 'react', 'ripple'];
const bytes = Buffer.byteLength(out);
for (const target of TARGETS) {
	const srcDir = path.resolve(__dirname, target, 'src');
	if (!fs.existsSync(srcDir)) continue;
	const dest = path.join(srcDir, 'data.js');
	fs.writeFileSync(dest, out);
	console.log(`wrote ${articles.length} articles → ${dest} (${(bytes / 1024).toFixed(1)} KB)`);
}
